"""
Update Metadata API endpoint.

Execute a previously generated update metadata action after user approval.
"""

from fastapi import APIRouter, HTTPException, Request, status

from app.core.logging_config import get_logger
from app.schemas.metadata_crud import (
    MetadataExecutionResponse,
    MetadataKeyActionRequest,
    UpdateMetadataAction,
)
from app.services.conpass_api_service import get_conpass_api_service
from app.utils.crud_metadata_hash_utils import decode_jwt_token

logger = get_logger(__name__)

router = APIRouter()


@router.post(
    "/update",
    summary="Execute Update Metadata Action",
    description="Execute a previously generated update metadata action after user approval",
    response_model=MetadataExecutionResponse,
    tags=["metadata-crud"],
)
async def execute_update_metadata(
    request: Request,
    payload: MetadataKeyActionRequest,
):
    """
    Execute an update metadata action.

    This endpoint is called by the frontend after the user approves an
    UpdateMetadataAction template generated by the agent's update_metadata tool.

    Args:
        request: FastAPI request object (contains auth token in state)
        payload: Contains jwt token containing the action data

    Returns:
        MetadataExecutionResponse with execution result
    """
    logger.info("[UPDATE_METADATA_API] Received update metadata request")

    try:
        # Get authentication token from request state (set by middleware)
        conpass_jwt = getattr(request.state, "conpass_token", None)
        if not conpass_jwt:
            logger.warning(
                "[UPDATE_METADATA_API] Authentication failed: No token in request state"
            )
            raise HTTPException(
                status_code=status.HTTP_401_UNAUTHORIZED,
                detail="Authentication required",
            )

        logger.info("[UPDATE_METADATA_API] Authentication validated.")

        if not payload.jwt_token:
            logger.warning(
                "[UPDATE_METADATA_API] Action authentication failed: No token in request payload"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Action token required",
            )

        logger.info("[UPDATE_METADATA_API] Action authentication validated.")

        try:
            action_metadata = decode_jwt_token(token=payload.jwt_token)
        except Exception as e:
            logger.error(f"[UPDATE_METADATA_API] Failed to decode JWT token: {e}")
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Invalid action token",
            )

        try:
            action = UpdateMetadataAction(**action_metadata)
        except Exception as e:
            logger.error(f"[UPDATE_METADATA_API] Failed to validate action data: {e}")
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST, detail="Invalid action data"
            )

        # Validate action has metadata items
        if not action.metadata_items:
            logger.warning(
                f"[UPDATE_METADATA_API] Validation failed: No metadata items in action "
                f"for contract {action.contract_id}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="No metadata items to update",
                error_details="The action contains no metadata items",
            )

        logger.debug(
            f"[UPDATE_METADATA_API] Converting {len(action.metadata_items)} metadata item(s) "
            f"to ConPass API format"
        )

        # Convert action items to ConPass API format
        metadata_list = []
        skipped_items = 0
        for idx, item in enumerate(action.metadata_items, 1):
            api_item = {}
            operation_type = None

            # Handle both UPDATE (metadata_id) and CREATE (key_id) operations
            if item.metadata_id is not None:
                # UPDATE existing metadata
                api_item["id"] = item.metadata_id
                operation_type = "UPDATE"
                logger.debug(
                    f"[UPDATE_METADATA_API] Item {idx}: UPDATE operation, "
                    f"metadata_id={item.metadata_id}, key_name={item.key_name}"
                )
            elif item.key_id is not None:
                # CREATE new metadata value for an empty field
                api_item["key_id"] = item.key_id
                operation_type = "CREATE"
                logger.debug(
                    f"[UPDATE_METADATA_API] Item {idx}: CREATE operation, "
                    f"key_id={item.key_id}, key_name={item.key_name}"
                )
            else:
                logger.error(
                    f"[UPDATE_METADATA_API] Item {idx}: Validation failed - "
                    f"missing both metadata_id and key_id. Item details: "
                    f"key_name={item.key_name}, value={item.value}, "
                    f"date_value={item.date_value}, lock={item.lock}"
                )
                skipped_items += 1
                continue

            # Add fields to API item
            fields_added = []
            if item.value is not None:
                api_item["value"] = item.value
                fields_added.append(
                    f"value='{item.value[:50]}{'...' if len(item.value) > 50 else ''}'"
                )

            if item.date_value is not None:
                api_item["dateValue"] = item.date_value
                fields_added.append(f"dateValue={item.date_value}")

            if item.lock is not None:
                api_item["lock"] = item.lock
                fields_added.append(f"lock={item.lock}")

            logger.debug(
                f"[UPDATE_METADATA_API] Item {idx}: {operation_type} operation prepared with "
                f"fields: {', '.join(fields_added) if fields_added else 'none'}"
            )

            metadata_list.append(api_item)

        if skipped_items > 0:
            logger.warning(
                f"[UPDATE_METADATA_API] Skipped {skipped_items} invalid item(s) "
                f"out of {len(action.metadata_items)} total"
            )

        if not metadata_list:
            logger.error(
                f"[UPDATE_METADATA_API] No valid metadata items after conversion. "
                f"All {len(action.metadata_items)} items were invalid or skipped"
            )
            return MetadataExecutionResponse(
                status="error",
                message="No valid metadata items to update",
                error_details="All metadata items are missing required metadata_id or key_id",
            )

        logger.info(
            f"[UPDATE_METADATA_API] Prepared {len(metadata_list)} valid metadata item(s) "
            f"for contract {action.contract_id}. Calling ConPass API..."
        )

        # Create ConPass API service and execute
        conpass_api_service = get_conpass_api_service(conpass_jwt)
        logger.debug(
            f"[UPDATE_METADATA_API] Calling update_contract_metadata API with "
            f"contract_id={action.contract_id}, items_count={len(metadata_list)}"
        )

        response = await conpass_api_service.update_contract_metadata(
            contract_id=action.contract_id,
            params={"list": metadata_list},
        )

        logger.info(
            f"[UPDATE_METADATA_API] ConPass API response received: "
            f"status={response.status}, "
            f"has_data={response.data is not None}, "
            f"description={response.description[:100] if response.description else 'None'}"
        )

        if response.status == "success":
            logger.info(
                f"[UPDATE_METADATA_API] Successfully updated metadata for contract {action.contract_id}. "
                f"Updated {len(metadata_list)} metadata item(s)"
            )
            logger.debug(f"[UPDATE_METADATA_API] Response data: {response.data}")
            return MetadataExecutionResponse(
                status="success",
                message=f"Successfully updated {len(metadata_list)} metadata item(s)",
                data=response.data,
            )
        else:
            logger.error(
                f"[UPDATE_METADATA_API] Failed to update metadata for contract {action.contract_id}. "
                f"Status: {response.status}, Description: {response.description}, "
                f"Data: {response.data}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to update metadata",
                error_details=response.description,
                data=response.data,
            )

    except HTTPException as e:
        logger.warning(
            f"[UPDATE_METADATA_API] HTTPException raised: status_code={e.status_code}, "
            f"detail={e.detail}"
        )
        raise
    except Exception as e:
        logger.exception(
            f"[UPDATE_METADATA_API] Unexpected error executing update metadata action "
            f"Error Details: {e}"
        )
        return MetadataExecutionResponse(
            status="error",
            message="Internal server error",
            error_details=str(e),
        )
