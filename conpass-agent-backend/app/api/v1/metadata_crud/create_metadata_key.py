"""
Create Metadata Key API endpoint.

Execute a create metadata key action after user approval.
"""

from fastapi import APIRouter, HTTPException, Request, status

from app.core.logging_config import get_logger
from app.schemas.metadata_crud import (
    CreateMetadataKeyAction,
    MetadataExecutionResponse,
    MetadataKeyActionRequest,
)
from app.services.conpass_api_service import get_conpass_api_service
from app.utils.crud_metadata_hash_utils import decode_jwt_token


router = APIRouter()
logger = get_logger(__name__)


@router.post(
    "/create-key",
    summary="Execute Create Metadata Key Action",
    description="Execute a create metadata key action after user approval",
    response_model=MetadataExecutionResponse,
    tags=["metadata-crud"],
)
async def execute_create_metadata_key(
    request: Request, payload: MetadataKeyActionRequest
):
    """
    Execute a create metadata key action.

    This endpoint is called by the frontend after the user approves a
    CreateMetadataKeyAction template generated by the agent's create_metadata_key tool.

    The execution process:
    1. Create the FREE MetaKey via POST /setting/meta/update

    Note: To enable this key for a directory, use update_directory_metadata_visibility separately.

    Args:
        request: FastAPI request object (contains auth token in state)
        payload: Contains jwt token containing the action data

    Returns:
        MetadataExecutionResponse with execution result
    """
    logger.info("[CREATE_METADATA_KEY_API] Received create metadata key request: ")
    try:
        # Get authentication token from request state (set by middleware)
        conpass_jwt = getattr(request.state, "conpass_token", None)
        if not conpass_jwt:
            logger.warning(
                "[CREATE_METADATA_KEY_API] Authentication failed: No token in request state"
            )
            raise HTTPException(
                status_code=status.HTTP_401_UNAUTHORIZED,
                detail="Authentication required",
            )

        logger.info("[CREATE_METADATA_KEY_API] Authentication validated. ")

        if not payload.jwt_token:
            logger.warning(
                "[CREATE_METADATA_KEY_API] Action authentication failed: No token in request payload"
            )

        logger.info("[CREATE_METADATA_KEY_API] Action authentication validated.")

        try:
            decoded_action = decode_jwt_token(token=payload.jwt_token)
        except Exception as e:
            logger.error(f"[CREATE_METADATA_KEY_API] Failed to decode JWT token: {e}")
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Invalid action token",
            )

        try:
            action_metadata = CreateMetadataKeyAction(**decoded_action)
        except Exception as e:
            logger.error(
                f"[CREATE_METADATA_KEY_API] Failed to validate action data: {e}"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST, detail="Invalid action data"
            )

        # Create ConPass API service
        conpass_api_service = get_conpass_api_service(conpass_jwt)

        # Create the FREE MetaKey (always visible)
        logger.info(
            f"[CREATE_METADATA_KEY_API] Creating FREE MetaKey '{action_metadata.names}' with is_visible=True"
        )
        logger.debug(
            f"[CREATE_METADATA_KEY_API] Calling create_meta_key API with "
            f"name='{action_metadata.names}', is_visible=True"
        )

        create_response = await conpass_api_service.create_meta_key(
            names=[action.name for action in action_metadata.names], is_visible=True
        )

        logger.info(
            f"[CREATE_METADATA_KEY_API] Response: status={create_response.status}, "
            f"has_data={create_response.data is not None}, "
            f"description={create_response.description[:100] if create_response.description else 'None'}"
        )

        if create_response.status != "success":
            logger.error(
                f"[CREATE_METADATA_KEY_API] Failed to create MetaKey '{action_metadata.names}'. "
                f"Status: {create_response.status}, Description: {create_response.description}, "
                f"Data: {create_response.data}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to create metadata key",
                error_details=create_response.description,
                data=create_response.data,
            )

        logger.info(
            f"[CREATE_METADATA_KEY_API] Successfully created metadata key '{action_metadata.names}'"
        )
        return MetadataExecutionResponse(
            status="success",
            message=f"Successfully created metadata key '{action_metadata.names}'",
            data={
                "name": action_metadata.names,
                "is_visible": True,
            },
        )

    except HTTPException as e:
        logger.warning(
            f"[CREATE_METADATA_KEY_API] HTTPException raised: status_code={e.status_code}, "
            f"detail={e.detail}"
        )
        raise
    except Exception as e:
        logger.exception(
            f"[CREATE_METADATA_KEY_API] Unexpected error executing create metadata key action "
            f"Error Details: {e}"
        )
        return MetadataExecutionResponse(
            status="error",
            message="Internal server error",
            error_details=str(e),
        )
