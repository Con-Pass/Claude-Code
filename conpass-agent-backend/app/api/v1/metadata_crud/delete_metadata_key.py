"""
Delete Metadata Key API endpoint.

Execute a delete metadata key action after user approval.
"""

from fastapi import APIRouter, HTTPException, Request, status

from app.core.logging_config import get_logger
from app.schemas.metadata_crud import (
    DeleteMetadataKeyAction,
    MetadataExecutionResponse,
    MetadataKeyActionRequest,
)
from app.services.conpass_api_service import get_conpass_api_service
from app.utils.crud_metadata_hash_utils import decode_jwt_token

router = APIRouter()

logger = get_logger(__name__)


@router.post(
    "/delete-key",
    summary="Execute Delete Metadata Key Action",
    description="Execute a delete metadata key action after user approval",
    response_model=MetadataExecutionResponse,
    tags=["metadata-crud"],
)
async def execute_delete_metadata_key(
    request: Request,
    payload: MetadataKeyActionRequest,
):
    """
    Execute a delete metadata key action.

    This endpoint is called by the frontend after the user approves a
    DeleteMetadataKeyAction template generated by the agent's delete_metadata_key tool.

    Args:
        request: FastAPI request object (contains auth token in state)
        payload: Contains jwt token containing the action data

    Returns:
        MetadataExecutionResponse with execution result
    """
    logger.info("[DELETE_METADATA_KEY_API] Received delete metadata key request: ")

    try:
        conpass_jwt = getattr(request.state, "conpass_token", None)
        if not conpass_jwt:
            logger.warning(
                "[DELETE_METADATA_KEY_API] Authentication failed: No token in request state"
            )
            raise HTTPException(
                status_code=status.HTTP_401_UNAUTHORIZED,
                detail="Authentication required",
            )

        logger.info("[DELETE_METADATA_KEY_API] Authentication validated. ")

        if not payload.jwt_token:
            logger.warning("[DELETED_METADATA_KEY_API] Action authentication failed.")
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Action token required",
            )
        logger.info("[DELETE_METADATA_KEY_API] Action authentication validated")

        try:
            decoded_action = decode_jwt_token(token=payload.jwt_token)
        except Exception as e:
            logger.error(f"[DELETE_METADATA_KEY_API] Failed to decode JWT token: {e}")
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Invalid action token",
            )

        try:
            action_metadata = DeleteMetadataKeyAction(**decoded_action)
        except Exception as e:
            logger.error(
                f"[DELETE_METADATA_KEY_API] Failed to validate action data: {e}"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST, detail="Invalid action data"
            )

        conpass_api_service = get_conpass_api_service(conpass_jwt)

        logger.info(
            f"[DELETE_METADATA_KEY_API] Deleting FREE MetaKey '{action_metadata.keys})"
        )

        delete_response = await conpass_api_service.delete_meta_key(
            key_ids=[int(action.key_id) for action in action_metadata.keys]
        )

        logger.info(
            f"[DELETE_METADATA_KEY_API] Response: status={delete_response.status}, "
            f"has_data={delete_response.data is not None}"
        )

        if delete_response.status != "success":
            logger.error(
                f"[DELETE_METADATA_KEY_API] Failed to delete MetaKey '{action_metadata.keys}'. "
                f"Status: {delete_response.status}, Description: {delete_response.description}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to delete metadata key",
                error_details=delete_response.description,
                data=delete_response.data,
            )

        logger.info(
            f"[DELETE_METADATA_KEY_API] Successfully deleted metadata key '{action_metadata.keys}'"
        )
        return MetadataExecutionResponse(
            status="success",
            message=f"Successfully deleted metadata key '{action_metadata.keys}'",
            data=delete_response.data
        )

    except HTTPException as e:
        logger.warning(
            f"[DELETE_METADATA_KEY_API] HTTPException raised: status_code={e.status_code}"
        )
        raise
    except Exception as e:
        logger.exception(
            f"[DELETE_METADATA_KEY_API] Unexpected error executing delete metadata key action"
            f"Error Details: {e}"
        )
        return MetadataExecutionResponse(
            status="error",
            message="Internal server error",
            error_details=str(e),
        )
