"""
Update Metadata Key API endpoint.

Execute an update metadata key action after user approval.
"""

from fastapi import APIRouter, HTTPException, Request, status

from app.core.logging_config import get_logger
from app.schemas.metadata_crud import (
    MetadataExecutionResponse,
    MetadataKeyActionRequest,
    UpdateMetadataKeyAction,
)
from app.services.conpass_api_service import get_conpass_api_service
from app.utils.crud_metadata_hash_utils import decode_jwt_token

router = APIRouter()

logger = get_logger(__name__)


@router.post(
    "/update-key",
    summary="Execute Update Metadata Key Action",
    description="Execute an update metadata key action after user approval",
    response_model=MetadataExecutionResponse,
    tags=["metadata-crud"],
)
async def execute_update_metadata_key(
    request: Request,
    payload: MetadataKeyActionRequest,
):
    """
    Execute an update metadata key action.

    This endpoint is called by the frontend after the user approves an
    UpdateMetadataKeyAction template generated by the agent's update_metadata_key tool.

    The execution process:
    1. Find the existing FREE MetaKey by name
    2. Update the MetaKey with new name via POST /setting/meta/update

    Note: This only updates the name. Visibility settings are preserved.

    Args:
        request: FastAPI request object (contains auth token in state)
        payload: Contains jwt token containing the action data

    Returns:
        MetadataExecutionResponse with execution result
    """
    logger.info("[UPDATE_METADATA_KEY_API] Received update metadata key request")

    try:
        # Get authentication token from request state (set by middleware)
        conpass_jwt = getattr(request.state, "conpass_token", None)
        if not conpass_jwt:
            logger.warning(
                "[UPDATE_METADATA_KEY_API] Authentication failed: No token in request state"
            )
            raise HTTPException(
                status_code=status.HTTP_401_UNAUTHORIZED,
                detail="Authentication required",
            )

        logger.info("[UPDATE_METADATA_KEY_API] Authentication validated.")

        if not payload.jwt_token:
            logger.warning(
                "[UPDATE_METADATA_KEY_API] Action authentication failed: No token in request payload"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Action token required",
            )

        logger.info("[UPDATE_METADATA_KEY_API] Action authentication validated.")

        try:
            action_metadata = decode_jwt_token(token=payload.jwt_token)
        except Exception as e:
            logger.error(f"[UPDATE_METADATA_KEY_API] Failed to decode JWT token: {e}")
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Invalid action token",
            )

        try:
            action = UpdateMetadataKeyAction(**action_metadata)
        except Exception as e:
            logger.error(
                f"[UPDATE_METADATA_KEY_API] Failed to validate action data: {e}"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST, detail="Invalid action data"
            )

        # Validate required fields
        if not action.key_id:
            logger.error(
                "[UPDATE_METADATA_KEY_API] Validation failed: key_id is required"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Invalid action: key_id is required",
                error_details="The action must include the key_id of the metadata key to update",
            )

        # Create ConPass API service
        conpass_api_service = get_conpass_api_service(conpass_jwt)

        # Get the metadata key details to preserve current settings
        logger.info(
            f"[UPDATE_METADATA_KEY_API] Fetching current metadata key details for key_id={action.key_id}"
        )
        keys_response = await conpass_api_service.get_free_metadata_keys()

        if keys_response.status != "success" or not keys_response.data:
            logger.error(
                f"[UPDATE_METADATA_KEY_API] Failed to fetch metadata keys. "
                f"Status: {keys_response.status}, Description: {keys_response.description}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to fetch metadata keys",
                error_details=keys_response.description,
                data=keys_response.data,
            )

        # Find the key by ID
        existing_keys = keys_response.data
        if isinstance(existing_keys, dict):
            existing_keys = existing_keys.get("response", [])

        found_key = None
        for key in existing_keys:
            if isinstance(key, dict) and key.get("id") == action.key_id:
                found_key = key
                break

        if not found_key:
            logger.error(
                f"[UPDATE_METADATA_KEY_API] Metadata key with id={action.key_id} not found"
            )
            return MetadataExecutionResponse(
                status="error",
                message=f"Metadata key with id={action.key_id} not found",
                error_details="The metadata key may have been deleted or the ID is invalid",
            )

        # Preserve current settings
        current_is_visible = found_key.get("is_visible", True)
        current_type = found_key.get("type", 2)
        current_status = found_key.get("status", 1)

        logger.info(
            f"[UPDATE_METADATA_KEY_API] Current key settings: "
            f"name='{found_key.get('name')}', is_visible={current_is_visible}, "
            f"type={current_type}, status={current_status}"
        )

        # Update the FREE MetaKey with new name, preserving other settings
        logger.info(
            f"[UPDATE_METADATA_KEY_API] Updating FREE MetaKey id={action.key_id} "
            f"with new name='{action.name}'"
        )
        logger.debug(
            f"[UPDATE_METADATA_KEY_API] Calling update_meta_key API with "
            f"key_id={action.key_id}, name='{action.name}', "
            f"is_visible={current_is_visible}, key_type={current_type}, status={current_status}"
        )

        update_response = await conpass_api_service.update_meta_key(
            key_id=action.key_id,
            name=action.name,
            is_visible=current_is_visible,
            key_type=current_type,
            status=current_status,
        )

        logger.info(
            f"[UPDATE_METADATA_KEY_API] Response: status={update_response.status}, "
            f"has_data={update_response.data is not None}, "
            f"description={update_response.description[:100] if update_response.description else 'None'}"
        )

        if update_response.status != "success":
            logger.error(
                f"[UPDATE_METADATA_KEY_API] Failed to update MetaKey id={action.key_id}. "
                f"Status: {update_response.status}, Description: {update_response.description}, "
                f"Data: {update_response.data}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to update metadata key",
                error_details=update_response.description,
                data=update_response.data,
            )

        logger.info(
            f"[UPDATE_METADATA_KEY_API] Successfully updated metadata key id={action.key_id} "
            f"from '{action.current_name}' to '{action.name}'"
        )
        return MetadataExecutionResponse(
            status="success",
            message=f"Successfully updated metadata key from '{action.current_name}' to '{action.name}'",
            data={
                "key_id": action.key_id,
                "old_name": action.current_name,
                "new_name": action.name,
                "is_visible": current_is_visible,
            },
        )

    except HTTPException as e:
        logger.warning(
            f"[UPDATE_METADATA_KEY_API] HTTPException raised: status_code={e.status_code}, "
            f"detail={e.detail}"
        )
        raise
    except Exception as e:
        logger.exception(
            f"[UPDATE_METADATA_KEY_API] Unexpected error executing update metadata key action "
            f"Error Details: {e}"
        )
        return MetadataExecutionResponse(
            status="error",
            message="Internal server error",
            error_details=str(e),
        )
