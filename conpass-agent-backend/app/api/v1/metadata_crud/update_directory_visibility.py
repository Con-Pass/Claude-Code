"""
Update Directory Metadata Visibility API endpoint.

Execute an update directory metadata visibility action after user approval.
"""

from fastapi import APIRouter, HTTPException, Request, status
from app.core.logging_config import get_logger
from app.schemas.metadata_crud import (
    MetadataExecutionResponse,
    MetadataKeyActionRequest,
    UpdateDirectoryMetadataVisibilityAction,
)
from app.services.conpass_api_service import get_conpass_api_service
from app.utils.crud_metadata_hash_utils import decode_jwt_token

router = APIRouter()

logger = get_logger(__name__)


@router.post(
    "/update-directory-visibility",
    summary="Execute Update Directory Metadata Visibility Action",
    description="Execute an update directory metadata visibility action after user approval",
    response_model=MetadataExecutionResponse,
    tags=["metadata-crud"],
)
async def execute_update_directory_metadata_visibility(
    request: Request,
    payload: MetadataKeyActionRequest,
):
    """
    Execute an update directory metadata visibility action.

    This endpoint is called by the frontend after the user approves an
    UpdateDirectoryMetadataVisibilityAction template generated by the agent's
    update_directory_metadata_visibility tool.

    The execution process:
    1. Fetch current directory metadata settings
    2. Merge updates with existing settings (preserve existing keys)
    3. Configure directory visibility via POST /setting/directory/meta/update

    Args:
        request: FastAPI request object (contains auth token in state)
        payload: Contains jwt token containing the action data

    Returns:
        MetadataExecutionResponse with execution result
    """
    logger.info(
        "[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Received update directory metadata visibility request"
    )

    try:
        # Get authentication token from request state (set by middleware)
        conpass_jwt = getattr(request.state, "conpass_token", None)
        if not conpass_jwt:
            logger.warning(
                "[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Authentication failed: No token in request state"
            )
            raise HTTPException(
                status_code=status.HTTP_401_UNAUTHORIZED,
                detail="Authentication required",
            )

        logger.info(
            "[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Authentication validated."
        )

        if not payload.jwt_token:
            logger.warning(
                "[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Action authentication failed: No token in request payload"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Action token required",
            )

        logger.info(
            "[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Action authentication validated."
        )

        try:
            action_metadata = decode_jwt_token(token=payload.jwt_token)
        except Exception as e:
            logger.error(
                f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Failed to decode JWT token: {e}"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail="Invalid action token",
            )

        try:
            action = UpdateDirectoryMetadataVisibilityAction(**action_metadata)
        except Exception as e:
            logger.error(
                f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Failed to validate action data: {e}"
            )
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST, detail="Invalid action data"
            )

        # Validate action has metadata key updates
        if not action.metadata_key_updates:
            logger.warning(
                f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Validation failed: No metadata key updates in action "
                f"for directory {action.directory_id}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="No metadata key updates to apply",
                error_details="The action contains no metadata key updates",
            )

        # Create ConPass API service
        conpass_api_service = get_conpass_api_service(conpass_jwt)

        # Step 1: Get current directory metadata settings
        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 1: Fetching current directory settings "
            f"for directory {action.directory_id}"
        )
        logger.debug(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Calling get_directory_metadata_settings API "
            f"with directory_id={action.directory_id}"
        )

        dir_settings_response = (
            await conpass_api_service.get_directory_metadata_settings(
                action.directory_id
            )
        )

        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 1 response: status={dir_settings_response.status}, "
            f"has_data={dir_settings_response.data is not None}"
        )

        if dir_settings_response.status != "success" or not dir_settings_response.data:
            logger.error(
                f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 1 failed: Failed to fetch directory settings "
                f"for directory {action.directory_id}. Status: {dir_settings_response.status}, "
                f"Description: {dir_settings_response.description}, Data: {dir_settings_response.data}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to fetch directory settings",
                error_details=(
                    f"Could not fetch directory {action.directory_id} settings. "
                    f"Status: {dir_settings_response.status}"
                ),
            )

        # Extract current settings
        dir_data = dir_settings_response.data
        response_data = dir_data.get("response", {})
        current_default_list = response_data.get("default_list", [])
        current_free_list = response_data.get("free_list", [])

        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 1 completed: Current directory has "
            f"{len(current_default_list)} default keys and {len(current_free_list)} free keys"
        )
        logger.debug(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Default keys: {[k.get('name') for k in current_default_list if isinstance(k, dict)]}, "
            f"Free keys: {[k.get('name') for k in current_free_list if isinstance(k, dict)]}"
        )

        # Step 2: Build updated lists by merging updates with existing settings
        # Create maps for quick lookup
        default_key_map = {
            key.get("id"): key
            for key in current_default_list
            if isinstance(key, dict) and key.get("id") is not None
        }
        free_key_map = {
            key.get("id"): key
            for key in current_free_list
            if isinstance(key, dict) and key.get("id") is not None
        }

        # Apply updates
        for update in action.metadata_key_updates:
            key_id = update.key_id
            if update.key_type == "DEFAULT":
                if key_id in default_key_map:
                    # Update existing
                    default_key_map[key_id]["is_visible"] = update.is_visible
                    if update.meta_key_directory_id is not None:
                        default_key_map[key_id]["meta_key_directory_id"] = (
                            update.meta_key_directory_id
                        )
                    logger.debug(
                        f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Updated DEFAULT key {key_id} "
                        f"visibility to {update.is_visible}"
                    )
                else:
                    # Add new (key not yet in directory)
                    default_key_map[key_id] = {
                        "id": key_id,
                        "name": update.key_name or "",
                        "type": "DEFAULT",
                        "is_visible": update.is_visible,
                        "meta_key_directory_id": update.meta_key_directory_id,
                    }
                    logger.debug(
                        f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Added new DEFAULT key {key_id} "
                        f"with visibility {update.is_visible}"
                    )
            else:  # FREE
                if key_id in free_key_map:
                    # Update existing
                    free_key_map[key_id]["is_visible"] = update.is_visible
                    if update.meta_key_directory_id is not None:
                        free_key_map[key_id]["meta_key_directory_id"] = (
                            update.meta_key_directory_id
                        )
                    logger.debug(
                        f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Updated FREE key {key_id} "
                        f"visibility to {update.is_visible}"
                    )
                else:
                    # Add new (key not yet in directory)
                    free_key_map[key_id] = {
                        "id": key_id,
                        "name": update.key_name or "",
                        "type": "FREE",
                        "is_visible": update.is_visible,
                        "meta_key_directory_id": update.meta_key_directory_id,
                    }
                    logger.debug(
                        f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Added new FREE key {key_id} "
                        f"with visibility {update.is_visible}"
                    )

        # Convert maps back to lists
        default_list = list(default_key_map.values())
        free_list = list(free_key_map.values())

        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 2: Prepared updated lists with "
            f"{len(default_list)} default keys and {len(free_list)} free keys"
        )

        # Step 3: Configure directory metadata
        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 3: Configuring directory {action.directory_id} "
            f"with updated visibility settings"
        )
        logger.debug(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Calling configure_directory_metadata API with "
            f"directory_id={action.directory_id}, default_list_count={len(default_list)}, "
            f"free_list_count={len(free_list)}"
        )

        config_response = await conpass_api_service.configure_directory_metadata(
            directory_id=action.directory_id,
            default_list=default_list,
            free_list=free_list,
        )

        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 3 response: status={config_response.status}, "
            f"has_data={config_response.data is not None}, "
            f"description={config_response.description[:100] if config_response.description else 'None'}"
        )

        if config_response.status != "success":
            logger.error(
                f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Step 3 failed: Failed to configure directory {action.directory_id}. "
                f"Status: {config_response.status}, Description: {config_response.description}, "
                f"Data: {config_response.data}"
            )
            return MetadataExecutionResponse(
                status="error",
                message="Failed to update directory metadata visibility",
                error_details=config_response.description,
                data=config_response.data,
            )

        logger.info(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] All steps completed successfully: "
            f"Updated visibility for {len(action.metadata_key_updates)} metadata key(s) "
            f"in directory {action.directory_id}"
        )
        return MetadataExecutionResponse(
            status="success",
            message=f"Successfully updated visibility for {len(action.metadata_key_updates)} metadata key(s) in the directory",
            data={
                "directory_id": action.directory_id,
                "updated_keys_count": len(action.metadata_key_updates),
            },
        )

    except HTTPException as e:
        logger.warning(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] HTTPException raised: status_code={e.status_code}, "
            f"detail={e.detail}"
        )
        raise
    except Exception as e:
        logger.exception(
            f"[UPDATE_DIRECTORY_METADATA_VISIBILITY_API] Unexpected error executing update directory metadata visibility action "
            f"Error Details: {e}"
        )
        return MetadataExecutionResponse(
            status="error",
            message="Internal server error",
            error_details=str(e),
        )
